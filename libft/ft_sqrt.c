#include "libft.h"

int		ft_sqrt(int nbr)
{
	int i;

	i = 1;
	if (nbr <= 0)
		return (0);
	while (i <= (nbr / 2))
	{
		if (i * i == nbr)
			return (i);
		i++;
	}
	return (ft_sqrt(nbr + 1));
}
